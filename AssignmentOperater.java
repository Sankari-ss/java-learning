class AssignmentOperater 
{
    public static void main(String[] args)
    {
            
            int x,y,z;
            x=10;
            y=20;
            z=25;
            x+=2;
            System.out.println(x);
            y-=3;
            System.out.println(y);
            z*=5;
            System.out.println(z);
};
}
